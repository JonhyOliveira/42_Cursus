#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*new;

	new = (t_list *)malloc(sizeof(t_list) * 1);
	if (new)
	{
		new->next = (t_list *) 0;
		new->content = (void *)content;
	}
	return (new);
}
